<?php
namespace xhtml;
/**
 * The HTML Emphasis Element (\<em>) marks text that has stress emphasis. 
 * 
 * The \<em> element can be nested, with each level of nesting indicating a 
 * greater degree of emphasis.
 * 
 * @example {
 *  <p>
 *      In HTML 5, what was previously called
 *      <em>
 *          block-level
 *      </em>
 *      content is now called
 *      <em>
 *          flow
 *      </em>
 *      content.
 *  </p>
 * }
 *
 * @note {
 *      Typically this element is displayed in italic type. However, it should
 *      not be used simply to apply italic styling; use the \<i> element, or
 *      CSS styling for that purpose.
 *
 *      Use the \<cite> element to mark the title of a work (book, play, song,
 *      etc.); it is also typically styled with italic type, but carries different
 *      meaning.
 *
 *      Use the \<strong> element to mark text that has greater importance than
 *      surrounding text.
 * }
 *
 * @link http://htmldog.com/reference/htmltags/em/
 * @link http://developer.mozilla.org/en/HTML/Element/em
 */
class Em extends abstractEntity\TextEntity
{
    /**
     * XML Tag Name
     *
     * @var string
     */
    protected $strTag = 'em';
    
    /**
     * The title attribute has the semantic meaning of the 
     * full description of the abbreviation.
     *
     * @xmlproperty title
     * @var String
     */
    protected $strTitle;

}
?>