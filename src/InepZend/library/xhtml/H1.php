/**
 * Heading elements implement six levels of document headings, \<h1> is the most 
 * important and \<h6> is the least. 
 * 
 * A heading element briefly describes the topic of the section it introduces. 
 * Heading information may be used by user agents, for example, to construct a 
 * table of contents for a document automatically.
 *
 * @example {
 *   <h1>Heading level 1</h1>
 *   <h2>Heading level 2</h2>
 *   <h3>Heading level 3</h3>
 *   <h4>Heading level 4</h4>
 *   <h5>Heading level 5</h5>
 *   <h6>Heading level 6</h6>
 * }
 *
 * @see xhtml\Div
 * @see xhtml\P
 * @see xhtml\Section
 * @link http://htmldog.com/reference/htmltags/h1/
 * @link http://developer.mozilla.org/en/HTML/Element/h1/
 */
class H1 extends abstractEntity\TextEntity
{
    /**
     * XML Tag Name
     *
     * @var string
     */
    protected $strTag = 'h1';
}
