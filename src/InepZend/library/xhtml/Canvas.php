<?php
namespace xhtml;
/**
 * The Canvas (\<canvas>)  HTML element  can be used to draw graphics via
 * scripting (usually JavaScript).
 *
 * For example, it can be used to draw graphs, make photo compositions or even
 * perform animations.
 * 
 * Mozilla applications gained support for \<canvas> starting with Gecko 1.8
 * (i.e., Firefox 1.5). 
 * 
 * The element was originally introduced by Apple for the OS X Dashboard and 
 * Safari. Internet Explorer, prior to version 9.0 beta, does not natively 
 * support \<canvas>, but a page can effectively add support for it by including
 * a script from Google's Explorer Canvas project. Opera 9 also supports \<canvas>.
 *
 * @note {
 *  The displayed size of the canvas can be changed using a stylesheet.
 *  The image is scaled during rendering to fit the styled size.
 * }
 *
 * @example {
 *  <canvas id="canvas" width="300" height="300">
 *      <div class="error">
 *          Sorry, your browser doesn't support the &lt;canvas&gt; element.
 *      </div>
 *  </canvas>
 * }
 *
 * @link http://developer.mozilla.org/en/HTML/Canvas
 * @link http://htmldog.com/reference/htmltags/canvas/
 * @link http://developer.mozilla.org/en/HTML/Element/canvas
 */
class Canvas extends Div
{
    /**
     * XML Tag Name
     *
     * @var string
     */
    protected $strTag = 'canvas';

    /**
     * The width of the coordinate space in CSS pixels.
     * Defaults to 300.
     *
     * @xmlProperty width
     * @var integer
     */
    protected $intWidth;

    /**
     * The height of the coordinate space in CSS pixels.
     * Defaults to 150.
     *
     * @xmlProperty height
     * @var integer
     */
    protected $intHeight;

}
?>